def sanitize_input(text: str) -> str:
    """Sanitize user input by removing potentially dangerous characters.

    This function removes HTML tags, script injections, and normalizes
    whitespace to prevent XSS attacks and other injection vulnerabilities.
    """
    # Remove HTML tags
    cleaned = re.sub(r'<[^>]+>', '', text)
    # Remove script patterns
    cleaned = re.sub(r'javascript:', '', cleaned, flags=re.IGNORECASE)
    # Normalize whitespace
    cleaned = ' '.join(cleaned.split())
    return cleaned.strip()


def calculate_fibonacci(n: int) -> List[int]:
    """Calculate Fibonacci sequence up to n numbers.

    Uses an iterative approach for O(n) time complexity and O(n) space.
    Returns the complete sequence as a list.
    """
    if n <= 0:
        return []
    if n == 1:
        return [0]

    sequence = [0, 1]
    for i in range(2, n):
        sequence.append(sequence[i-1] + sequence[i-2])
    return sequence


